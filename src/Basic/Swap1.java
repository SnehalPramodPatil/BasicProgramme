package Basic;

public class Swap1 {
      public static void main(String args[]) {
    	  int a=35;
    	  int b=45;
    	  System.out.println("No before swapping");
    	  System.out.println("a="+a);
    	  System.out.println("b="+b);
//    	  a=a+b;
//    	  b=a-b;
//    	  a=a-b;
    	  a=a*b;
    	  b=a/b;
    	  a=a/b;
    	  System.out.println("No after swapping");
    	  System.out.println("a="+a);
    	  System.out.println("b="+b);
    	  
    	  
      }
}
