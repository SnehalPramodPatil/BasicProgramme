package Basic;

public class MetroCity {
      public static void main(String args[]) {
    	  String city="Pune";
    	  
    	  if(city.equals("Pune"))
    	  {
    		System.out.println(city+" " +"is a Metro city");  
    	  }
    	  else if(city.equals("Mumbai"))
    	  {
    		  System.out.println(city+" "+"is a Metro city");
    	  }
    	  else if(city.equals("Nagpur"))
    	  {
    		  System.out.println(city+" "+"is a Metro city");
    	  }
    	  else
    	  {
    		  System.out.println(city+" "+"is a non Metro city");
    	  }
      }
}
