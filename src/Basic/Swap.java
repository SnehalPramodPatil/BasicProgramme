package Basic;

public class Swap {
      public static void main(String args[]) {
    	  int a=95;
    	  int b=55;
    	  int c;
    	  System.out.println("No before Swapping");
     	  System.out.println("a="+a);
     	  System.out.println("b="+b);
    	  c=a;
    	  a=b;
    	  b=c;
    	  
    	 System.out.println("No after Swapping");
    	 System.out.println("a="+a);
    	 System.out.println("b="+b);
    	 
      }
}
